// RewardValue.java

public class RewardValue {
    // Member variable
    private double value;

    // Constructor
    public RewardValue(double value) {
        this.value = value;
    }

    // Method to get the reward value
    public double getValue() {
        return value;
    }

    // Method to set the reward value
    public void setValue(double value) {
        this.value = value;
    }
}
